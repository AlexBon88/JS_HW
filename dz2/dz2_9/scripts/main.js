let key = prompt('enter the day of the week');
switch (key) {
    case 'monday':
        console.log('monday');
        break;
    case 'tuesday':
        console.log('tuesday');
        break;
    case 'wednesday':
        console.log('wednesday');
        break;
    case 'thursday':
        console.log('thursday');
        break;
    case 'friday':
        console.log('friday');
        break;
    case 'saturday':
        console.log('saturday');
        break;
    case 'sunday':
        console.log('sunday')
}